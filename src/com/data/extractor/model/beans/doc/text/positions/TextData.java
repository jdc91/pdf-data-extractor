package com.data.extractor.model.beans.doc.text.positions;


public class TextData {

    private String character;
    private float xDirAdj;
    private float yDirAdj;
    private float fontSize;
    private float xScale;
    private float heightDir;
    private float widthOfSpace;
    private float widthDirAdj;

    public float getxDirAdj() {        return xDirAdj;    }

    public void setxDirAdj(float xDirAdj) {        this.xDirAdj = xDirAdj;    }

    public float getyDirAdj() {        return yDirAdj;    }

    public void setyDirAdj(float yDirAdj) {        this.yDirAdj = yDirAdj;    }

    public float getFontSize() {        return fontSize;    }

    public void setFontSize(float fontSize) {        this.fontSize = fontSize;    }

    public float getxScale() {        return xScale;    }

    public void setxScale(float xScale) {        this.xScale = xScale;    }

    public float getHeightDir() {        return heightDir;    }

    public void setHeightDir(float heightDir) {        this.heightDir = heightDir;    }

    public float getWidthOfSpace() {        return widthOfSpace;    }

    public void setWidthOfSpace(float widthOfSpace) {        this.widthOfSpace = widthOfSpace;    }

    public float getWidthDirAdj() {        return widthDirAdj;    }

    public void setWidthDirAdj(float widthDirAdj) {        this.widthDirAdj = widthDirAdj;    }

    public String getCharacter() {        return character;    }

    public void setCharacter(String character) {        this.character = character;    }

}
